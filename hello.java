
class Main {

    public static void main(String[] args) {

        // create variables
        int a = 7;
        int b = 11;

        // value of a nd b
        System.out.println("a is " + a + " and b is " + b); 

        // == operator
        System.out.println("a == b : " + (a == b)); // false

        // != operator
        System.out.println("a != b : " + (a != b)); // true

        // > operator
        System.out.println("a > b : " + (a > b)); // false

        // < operator
        System.out.println("a < b : " + (a < b)); // true

        // >= operator
        System.out.println("a >= b : " + (a >= b)); // false

        // <= operator
        System.out.println("a <= b : " + (a <= b)); // true
    }

}