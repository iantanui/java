
class Main {

    public static void main(String[] args) {

        int a = 4;
        int var;

        var = a; // assign using =
        System.out.println("var using = : " + var); 

        var += a; // assign using =+
        System.out.println("var using += : " + var); 

        var *= a; // assign using =*
        System.out.println("var using *= : " + var); 
    }

}